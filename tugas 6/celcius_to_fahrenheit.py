# input suhu celcius

# Langkah 1 : Membuat variabel untuk menyimpan celcius
celcius = float(input("Masukan suhu celcius : "))  # operand celcius

# Langkah 2 : Mengubah celcius ke fahrenheit
# proses konversi
fahrenheit = (celcius * 9/5) + 32  # operator 

# output
# Langkah 3 : Menampilkan suhu fahrenheit
print(f"Suhu dalam celcius adalah : {celcius}")
print(f"Suhu dalam fahrenheit adalah : {fahrenheit}")