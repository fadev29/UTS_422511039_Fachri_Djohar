# program untuk menghitung total harga barang

# mengambil input dari penguna
harga = float(input("Masukan harga barang : "))  # operand harga
jumlah = int(input("Masukan jumlah barang : "))  # operand jumlah

# menghitung total harga
total_harga = harga * jumlah  # operator * perkalian

# menampilkan hasil nya 
print(f"Total harga adalah : {total_harga}") 

# runtunan Sequence :
# 1. mengambil input dari penguna
# 2. menghitung total harga