# Langka 1 : Membuat variabel untuk menyimpan angka
angka = int(input("Masukan angka  Bulan: "))  # operand

# Langkah 2 : Menentukan kategori angka bulan
if angka == 1:    # operator relasional mententukan kategori bulan
    print("Januari")
elif angka == 2:
    print("Februari")
elif angka == 3:
    print("Maret")
elif angka == 4:
    print("April")
elif angka == 5:
    print("Mei")
elif angka == 6:
    print("Juni")
elif angka == 7:
    print("Juli")
elif angka == 8:
    print("Agustus")
elif angka == 9:
    print("September")
elif angka == 10:
    print("Oktober")
elif angka == 11:
    print("November")
elif angka == 12:
    print("Desember")
else:
    print("Bulan tidak valid")

